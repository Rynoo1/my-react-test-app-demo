//Implement functionality to add two numbers
    export const addNumbers = (a: number, b: number): number => {
        return a + b
    }


//Implement functionality to subtract two numbers
    export const subtractNumbers = (a: number, b: number): number => {
        return a - b
    }

//Implement functionality to multiply three numbers
    export const multiplyNumbers = (a: number, b: number, c: number): number => {
        return a * b * c
    }